export const singleCategoryController = async (req, res) => {
  try {
    const category = await categoryModel.findOne({ slug: req.params.slug });
    if (!category) {
      res.status(500).send({
        success: false,
        message: "Error while getting single category",
      });
    }
    res.status(200).send({
      success: true,
      category,
      message: "Display single category successfully",
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error while getting single category",
    });
  }
};
